use std::net::SocketAddr;
use std::sync::{Arc, OnceLock};
use std::time::Duration;

use quinn::{ClientConfig, Endpoint, ServerConfig};
use rustls::pki_types::{CertificateDer, PrivateKeyDer, ServerName, UnixTime};

/// Initialize the rustls crypto provider once
/// This must be called before any QUIC operations
pub fn init_crypto_provider() {
    // Only install if not already installed
    if rustls::crypto::ring::default_provider()
        .install_default()
        .is_ok()
    {
        tracing::debug!("Installed ring crypto provider for rustls");
    }
}

/// Cached QUIC client config for reuse across all outgoing connections.
/// This config uses the InsecureCertVerifier since identity verification
/// is performed at the application layer via extract_peer_identity().
static QUIC_CLIENT_CONFIG: OnceLock<quinn::ClientConfig> = OnceLock::new();

/// Get or initialize the cached QUIC client config.
/// The config is safe to cache because it doesn't depend on per-connection state -
/// all identity verification happens at the application layer.
fn get_or_init_client_config() -> quinn::ClientConfig {
    QUIC_CLIENT_CONFIG
        .get_or_init(|| {
            let mut client_config = rustls::ClientConfig::builder()
                .dangerous()
                .with_custom_certificate_verifier(Arc::new(InsecureCertVerifier))
                .with_no_client_auth();
            client_config.alpn_protocols = vec![b"irondag/1".to_vec()];

            ClientConfig::new(Arc::new(
                quinn::crypto::rustls::QuicClientConfig::try_from(client_config)
                    .expect("Failed to create QUIC client config"),
            ))
        })
        .clone()
}

